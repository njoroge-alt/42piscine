#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n < 10)
	{
		ft_putchar(n + '0');
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	unsigned int	i;

	i = -1;
	while ((par + i)->str != 0)
	{
		ft_putstr((par + i)->str);
		ft_putchar('\n');
		ft_putnbr((par + i)->size);
		ft_putchar('\n');
		ft_putstr((par + i)->copy);
		ft_putchar('\n');
	}
}
